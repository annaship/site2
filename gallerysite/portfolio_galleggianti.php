    <p>&quot;You&#39;re an artist&nbsp;&amp; I&#39;m a geologist. Let&#39;s see what happens when a scientist&nbsp;&amp; an artist collaborate&nbsp;&amp; what they can come up with,&quot; Dr. Susumu Honjo, a Senior Scientist at the Woods Hole Oceanographic Institution told me when I fist moved to Cape Cod. </p>
    <p>Sus was researching microscopic sea organisms&nbsp;&amp; the effects of increased greenhouse gases on marine life. I was exploring the nature of photography.</p>
    <p>Sus&nbsp;&amp; his colleagues were using a Scanning Electron Microscope (SEM) to learn about radiolaria, coccoliths,&nbsp;&amp; diatoms. Sus told me I could use his archive of SEM negatives to play with. I wanted to make photographs that were as soft, wet, diaphanous, irregular in outline, and sculptural as the swimming sea creatures Sus was studying.</p>
    <p>I explored the basement of the Marine Biological Lab&nbsp;&amp; found some old hand-blown glass containers about to be discarded. I found formulas for photographic emulsions in 19<sup>th</sup> c. books at the MBL library. I began to experiment with silver nitrate, gelatin,&nbsp;&amp; organic chemicals. Instead of putting my hand-made light sensitive emulsions onto a stiff paper surface, I poured them onto waxed paper. I exposed them to light under enlarged negatives I made from Sus&#39; SEM images. </p>
    <p>Then I floated the membrane-like photographs in water in the glass containers. I had created an image of a radiolarian that swam in water like the creature itself. My first show of this work was called &quot;The Dissolution of a Coccolith, or the Birth of a Photographic Idea.&quot;</p>
    <p>I was pregnant with my youngest daughter at the time. These floating emulsions, I call them &quot;Galleggianti&quot;&nbsp;&#8212; Italian for things that float&nbsp;&#8212; felt to me to be like embryos. </p>
    <p>I returned to my studies with Sonia Landy Sheridan, my mentor at the Generative Systems Department of the School of the Art Institute of Chicago. I did a series of self-portraits, nudes, and images of the fetal heartbeat of my unborn child. Cheered on by Sonia, I used copy machines, the original Xerox Haloid machine, ultra-sound microphones, and the proto-Photoshop Sandine Image Processor to make negatives that I transferred to my gelatin emulsions. I call this my Placenta Series.</p>
    <p>I have bags of these three-dimensional emulsions in my basement. Left too long in water, they indeed become alive&nbsp;&#8212; with mold. Not the kind of life I wanted to image. Dried, they look like shrunken heads. But after 15 minutes in water, they are as graceful as jellyfish.</p>
    <p>I am grateful to Dr. Susumu Honjo for opening up new imaging possibilities. I will forever love Sonia Landy Sheridan for her unending inspiration&nbsp;&amp; for her challenge to me to re-invent photography&nbsp;&amp; make it ever more human.</p>

